/*!
 * \file main.cpp
 * \brief Точка входа клиентского приложения.
 */
//! \brief Файл, содержащий главную функцию main - точку входа в программу

#include <QApplication>
//! \brief Подключение класса приложения Qt, управляющего событийным циклом GUI

#include "mainwindow.h"
//! \brief Подключение заголовочного файла главного окна приложения

#include "tcpclient.h"
//! \brief Подключение заголовочного файла TCP-клиента (Singleton)

int main(int argc, char *argv[]) {
    //! \brief Главная функция - точка входа в программу
    //! \param argc Количество аргументов командной строки
    //! \param argv Массив строк с аргументами командной строки
    //! \return Код возврата (0 при успешном завершении, иначе код ошибки)

    QApplication a(argc, argv);
    //! \brief Создает объект приложения Qt, который управляет ресурсами и событийным циклом
    //! \param argc Количество аргументов командной строки
    //! \param argv Массив аргументов командной строки

    a.setApplicationName("TCP Math Client");
    //! \brief Устанавливает имя приложения (используется в настройках и заголовках окон)

    a.setOrganizationName("TIMP");
    //! \brief Устанавливает название организации-разработчика (используется в настройках приложения)

    MainWindow w;
    //! \brief Создает главное окно приложения (контроллер, управляющий формами)

    w.show();
    //! \brief Отображает главное окно на экране

    int result = a.exec();
    //! \brief Запускает главный событийный цикл Qt
    //! \details Функция блокирует выполнение, пока приложение не будет закрыто.
    //!          Возвращает код завершения приложения (обычно 0 при успехе)
    //! \return Код завершения приложения

    // Чистое отключение от сервера при выходе
    TcpClient::getInstance()->disconnectFromServer();
    //! \brief Получает единственный экземпляр TCP-клиента и отключает его от сервера
    //! \details Выполняется после завершения событийного цикла, чтобы корректно закрыть
    //!          сетевое соединение перед завершением приложения

    return result;
    //! \brief Возвращает код завершения приложения операционной системе
}
