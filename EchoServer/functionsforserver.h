#ifndef FUNCTIONSFORSERVER_H
//! \brief Защита от множественного включения заголовочного файла

#define FUNCTIONSFORSERVER_H
//! \brief Определение макроса для защиты от повторного включения

#include <QString>
//! \brief Подключение класса для работы со строками в Qt

#include <QStringList>
//! \brief Подключение класса для работы со списком строк

#include <QVector>
//! \brief Подключение класса для работы с динамическим массивом

/*!
 * \file functionsforserver.h
 * \brief Заголовочный файл функциональной части сервера.
 *
 * Содержит разбор протокола, математические вычисления кусочной функции
 * и проверку ответов пользователя. Поддерживает динамические параметры функций.
 */
//! \brief Файл заголовка, содержащий математические функции и утилиты для сервера

/*!
 * \brief Разбирает входящее сообщение и формирует ответ сервера.
 *
 * Поддерживает команды: auth, reg, stat, check, confirm, getparams, setparams.
 * \param message Входящее сообщение от клиента
 * \return Строка-ответ сервера
 */
QString parsing(const QString &message);
//! \brief Функция парсинга и обработки команд от клиента
//! \param message Входящее сообщение от клиента в формате "команда&параметры"
//! \return Строка-ответ сервера (например, "auth+", "check+", "stat&3$6&21" и т.д.)

/*!
 * \brief Вычисляет значение кусочной функции f(x) с текущими параметрами.
 *
 * f1(x) = a1 * ln(x + b1) + c1,    -1 < x < 0
 * f2(x) = a2 * x³ + b2 * x + c2,    0 <= x <= 2
 * f3(x) = a3/(x + b3) + c3,         x > 2
 *
 * \param x Аргумент функции
 * \return Значение функции (или NaN для недопустимых x)
 */
double computeF(double x);
//! \brief Вычисляет значение кусочной функции f(x) с глобальными параметрами
//! \param x Аргумент функции (действительное число)
//! \return Значение функции для заданного x, или NaN, если x вне области определения
//! \details Поддерживает три кусочные функции с динамически изменяемыми параметрами

/*!
 * \brief Численно решает уравнение f(x) = 0 методом бисекции.
 * \param a Левая граница отрезка
 * \param b Правая граница отрезка
 * \param eps Точность (по умолчанию 1e-6)
 * \return Приближённый корень или NaN если нет смены знака
 */
double bisect(double a, double b, double eps = 1e-6);
//! \brief Находит корень уравнения f(x)=0 методом бисекции (деления пополам)
//! \param a Левая граница интервала
//! \param b Правая граница интервала
//! \param eps Требуемая точность вычислений (по умолчанию 1e-6)
//! \return Приближенное значение корня, если на интервале есть смена знака,
//!         иначе возвращает NaN (Not a Number)

/*!
 * \brief Находит все корни функции на заданном интервале.
 * \param start Начало интервала
 * \param end Конец интервала
 * \param step Шаг сканирования
 * \return Вектор найденных корней
 */
QVector<double> findRoots(double start, double end, double step = 0.01);
//! \brief Находит все корни уравнения f(x)=0 на заданном интервале
//! \param start Начало интервала для поиска корней
//! \param end Конец интервала для поиска корней
//! \param step Шаг сканирования для обнаружения смены знака (по умолчанию 0.01)
//! \return Вектор всех найденных корней (уникальные значения с точностью до шага)

/*!
 * \brief Проверяет ответ пользователя на задачу.
 * \param taskNumber Номер задачи (1-3)
 * \param variant    Номер варианта (используется для задачи 2)
 * \param answer     Ответ пользователя (строка с числом)
 * \return true если ответ верный (с точностью 1e-2)
 */
bool checkAnswer(int taskNumber, int variant, const QString &answer);
//! \brief Проверяет правильность ответа пользователя на математическую задачу
//! \param taskNumber Номер задачи (1, 2 или 3)
//! \param variant Номер варианта (для задачи 2: 1 - первый корень, 2 - второй корень)
//! \param answer Ответ пользователя в виде строки (преобразуется в число с плавающей точкой)
//! \return true - ответ верный (погрешность не более 0.01), false - неверный ответ

/*!
 * \brief Устанавливает параметры для всех трёх функций.
 * \param params Список параметров: [cmd, a1, b1, c1, a2, b2, c2, a3, b3, c3]
 * \return true если параметры успешно установлены
 */
bool setFunctionParams(const QStringList &params);
//! \brief Устанавливает коэффициенты для всех трех кусочных функций
//! \param params Список строк с параметрами формата: [команда, a1, b1, c1, a2, b2, c2, a3, b3, c3]
//! \return true - параметры успешно установлены, false - ошибка парсинга
//! \details Параметры хранятся в глобальных переменных для использования в computeF()

/*!
 * \brief Возвращает строку с текущими параметрами функций.
 * \return Строка вида "params&a1&b1&c1&a2&b2&c2&a3&b3&c3"
 */
QString getFunctionParams();
//! \brief Формирует строку с текущими параметрами всех функций
//! \return Строка в формате "params&a1&b1&c1&a2&b2&c2&a3&b3&c3"
//! \details Используется для отправки параметров клиенту по запросу

/*!
 * \brief Получает правильный корень для задачи с текущими параметрами.
 * \param taskNumber Номер задачи (1-3)
 * \param variant Номер варианта (для задачи 2: 1 или 2)
 * \return Значение корня или NaN если корень не найден
 */
double getCorrectRoot(int taskNumber, int variant = 1);
//! \brief Вычисляет правильный корень для указанной задачи с текущими параметрами
//! \param taskNumber Номер задачи (1, 2 или 3)
//! \param variant Номер варианта (для задачи 2: 1 - первый корень, 2 - второй корень)
//! \return Значение корня, или NaN (Not a Number), если корень не найден
//! \details Для задачи 1: корень на интервале [0, 2] (первый)
//!          Для задачи 2: корень на интервале [0, 2] (второй, больший)
//!          Для задачи 3: корень на интервале (2, ∞)

#endif // FUNCTIONSFORSERVER_H
//! \brief Конец защиты от множественного включения заголовочного файла
